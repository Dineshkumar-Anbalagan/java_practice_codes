public class JavaDtypes{
    public static void main(String[] args){
        String call_me_on = "8056732411"; //anything wiht reference datatype starts with a capital letter
        String name = "Dineshkumar";
        long x = 123434212334L; //anything with a primitice datatype starts with small letter
        float y = 12.234234f;
        double z = 12.324234;
        char symbol = '@';
        System.out.println("Hi! This is Dinesh : "+name);
        System.out.println("Hmu on ==>"+call_me_on);
        System.out.println("My leaderboard score is : "+x);
        System.out.println("The overall score average : "+y);
        System.out.println("The overall score using double : "+z);
        System.out.println("printing symbol : "+symbol);
    }
}
